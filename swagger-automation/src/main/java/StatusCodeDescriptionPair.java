public class StatusCodeDescriptionPair {
    private String code;
    private String description;


    //This is a container object representing status code/description pairs

    public StatusCodeDescriptionPair(String code, String description) {
        this.code = code;
        this.description = description;
    }

    public String getCode() {
        return code;
    }

    public String getDescription() {
        return description;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
